package ch02.sec01;

public class VariableInitializationExample {
	public static void main(String[] args) {
		int value;		//value 변수 선언
		int result = value + 10;			//value변수의 저장된 값과 10을 더해서 
		                                    //result변수에 집어넣어라
		                                    //5행에 변수선언은 존재하지만 
		                                    //value값이 얼마냐는 값이 나오지 않아서
		System.out.println(result);
	}
}
